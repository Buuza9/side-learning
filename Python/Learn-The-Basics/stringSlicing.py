# It's the process of extracting a portion of a string or work with only a specific part of it

my_str = "Hello Mohammed"

print(my_str[0])  # H
print(my_str[3])  # l

# you can extract characters from a certain index
print(my_str[0:3])  # Hel

#You can reverse a string by the step parameter, by setting step tp -1 adn leaving start and end to blank
print(my_str[::-1])